
public class Carta {
	
	int naipe, numero;
	
	public Carta() {
		naipe = 0;
		numero = 0;
	}

	/**
	 * @return the naipe
	 */
	public int getNaipe() {
		return naipe;
	}

	/**
	 * @param naipe the naipe to set
	 */
	public void setNaipe(int naipe) {
		this.naipe = naipe;
	}

	/**
	 * @return the numero
	 */
	public int getNumero() {
		return numero;
	}

	/**
	 * @param numero the numero to set
	 */
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
}
